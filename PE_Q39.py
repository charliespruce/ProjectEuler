def numofsols(p):
    total = 0
    for a in ptrips:
        if a < p:
            for b in ptrips:
                if b < p - a:
                    if a + b + round(sqrt(a**2 + b**2),5) == p:
                        total += 1
                        #print(str(a) + ' - ' + str(b))
    return total//2

